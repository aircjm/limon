struct User {
    username: String,
    email: String,
    sign_in_count: u64,
    active: bool,
}

fn build_user(email: String, username: String) -> User {
    User {
        email,
        username,
        active: true,
        sign_in_count: 1,
    }
}

fn main() {
    let user = build_user("anyone@example.com".to_string(),
                           "anyone".to_string());

    println!("{}", user.username);

}
